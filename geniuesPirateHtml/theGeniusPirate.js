		function createQuestion() {
			seconds = 30; //איפוס הטיימר ל 30 שניות בכל שאלה חדשה שנוצרת
			//הוספת השאלה
			randQuestions = parseInt(Math.random() * (myGame.length - 1));
			var textQuestion = new createjs.Text();
			stage.addChild(textQuestion);
			textQuestion.name = "textQuestion";
			textQuestion.color = "black";
			textQuestion.text = myGame[randQuestions][0][0];
			textQuestion.textAlign = "right";
			textQuestion.font = "18pt Assistant";
			textQuestion.x = 970;
			textQuestion.y = 100;
			textQuestion.lineWidth = 260;
			//הוספת מיקומים לתמונות בשאלה במידה וקיימות
			if (myGame[randQuestions][0][1] != "noimg") {
				//יצירת התמונות בשאלה
				var imgQuestion = new(myGame[randQuestions][0][1]);
				stage.addChild(imgQuestion);
				imgQuestion.name = "imgQuestion";
				imgQuestion.x = 590;
				imgQuestion.y = 100;
				imgQuestion.scaleX = 0.09;
				imgQuestion.scaleY = 0.09;
				//יצירת זכוכית המגדלת
				var mgglassQues = new lib.mgglass;
				stage.addChild(mgglassQues);
				mgglassQues.name = "mgglassQues";
				mgglassQues.x = 583;
				mgglassQues.y = 80
				mgglassQues.scaleX = 0.05;
				mgglassQues.scaleY = 0.05;
				mgglassQues.addEventListener("mouseover", mgglassQuesHover);
				mgglassQues.addEventListener("mouseout", mgglassQuesHoverOut);
			}
			//יצירת המסיחים בהגרלה
			//הלולאה מתחילה ב1 כי השאלה נמצאת במקום האפס
			//הלולאה נגמרת באורך כמות המסיחים של השאלה שהוגרלה
			for (i = 1; i < myGame[randQuestions].length; i++) {
				//הגרלת שאלה
				randAnswers = Math.floor(Math.random() * (myGame[randQuestions].length - 1) + 1);
				//לולאה שבודקת האם למסיח יש 1 במיקום ה4, ואם כן, א ז מגרילה שוב מסיח
				while (myGame[randQuestions][randAnswers][3] == 1) {
					randAnswers = Math.floor(Math.random() * (myGame[randQuestions].length - 1) + 1);
				}
				//אם למסיח אין 1 במקום ה4 אז  זה אומר שהוא לא הוגרל ונשים לו 1
				myGame[randQuestions][randAnswers][3] = 1;
		
				//יצירה דינאמית של המסיחים
				var treasureBox = new lib.treasureBox;
				stage.addChild(treasureBox);
				treasureBox.name = "treasureBox" + (i - 1);
				treasureBox.y = 625 //מיקום האורך לא משתנה
				//הוספת מאזין ללחיצה על תיבה
				treasureBox.addEventListener("click", clickTreasureBox);
				treasureBox.cursor = "pointer"; //שינוי מצביע העכבר 
				//שמירת המסיח המכיל את התשובה הנכונה במשתנה ייעודי
				if (myGame[randQuestions][randAnswers][1] == true) {
					correctBox = treasureBox.name.charAt(11);
				}
				//יצירת הטקסט על המסיחים
				var treasureBoxText = new createjs.Text();
				treasureBox.addChild(treasureBoxText);
				treasureBoxText.name = "treasureBoxText";
				//מיקום דינאמי לפי כמות המסיחים
				stage.getChildByName("treasureBox" + (i - 1)).x = ((1280 / (myGame[randQuestions].length - 1)) / 2) + ((1280 / (myGame[randQuestions].length - 1)) * (i - 1));
				//בדיקה האם יש במסיח תמונה
				if (myGame[randQuestions][randAnswers][2] == "img") {
					var imgAnswer = new(myGame[randQuestions][randAnswers][0]);
					//הגדרת מיקום תמונה במסיח
					stage.addChild(imgAnswer);
					imgAnswer.name = "imgAnswer" + i;
					imgAnswer.x = stage.getChildByName("treasureBox" + (i - 1)).x;
					imgAnswer.y = 650;
					//יצירת זכוכית מגדלת במסיחים
					var mgglass = new lib.mgglass;
					treasureBox.addChild(mgglass);
					mgglass.name = "mgglass" + i;
					mgglass.x = -90;
					mgglass.y = -10;
					mgglass.scaleX = 0.05;
					mgglass.scaleY = 0.05;
					mgglass.addEventListener("mouseover", mgglassHover);
					mgglass.addEventListener("mouseout", mgglassHoverOut);
					//במידה ואין תמונה הדפסת הטקסט
				} else {
					treasureBoxText.text = myGame[randQuestions][randAnswers][0];
				}
		
				// שינוי גודל התיבות בהתאם לכמות המסיחים
				if ((myGame[randQuestions].length - 1) == 4) {
					stage.getChildByName("treasureBox" + (i - 1)).scaleX = 1.2;
					stage.getChildByName("treasureBox" + (i - 1)).scaleY = 1.2;
					treasureBoxText.lineWidth = 160;
					treasureBoxText.font = "16pt Assistant";
					treasureBoxText.textAlign = "center";
				} else if ((myGame[randQuestions].length - 1) == 5) {
					stage.getChildByName("treasureBox" + (i - 1)).scaleX = 1;
					stage.getChildByName("treasureBox" + (i - 1)).scaleY = 1;
					treasureBoxText.font = "16pt Assistant";
					treasureBoxText.textAlign = "center";
					treasureBoxText.lineWidth = 160;
				} else if ((myGame[randQuestions].length - 1) == 6) {
					stage.getChildByName("treasureBox" + (i - 1)).scaleX = 0.8;
					stage.getChildByName("treasureBox" + (i - 1)).scaleY = 0.8;
					treasureBoxText.lineWidth = 200
					treasureBoxText.font = "16pt Assistant";
					treasureBoxText.textAlign = "center";
				}
			}
			timerGame();
		}
